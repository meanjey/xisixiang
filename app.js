function loadProgress() {
  try {
    const parsed = JSON.parse(localStorage.getItem(storageKey));
    return {
      stats: parsed?.stats || {},
      mistakes: parsed?.mistakes || {},
      favorites: parsed?.favorites || {},
      lastIndex: parsed?.lastIndex || 0,
    };
  } catch {
    return { stats: {}, mistakes: {}, favorites: {}, lastIndex: 0 };
  }
}

function saveProgress() {
  localStorage.setItem(storageKey, JSON.stringify(progress));
}

function getChapter(chapterId) {
  return questionBank.find((chapter) => chapter.id === chapterId) || questionBank[0];
}

function getCurrentQuestion() {
  return state.queue[state.index];
}

function getQuestionId(question) {
  return question.id;
}

function normalizeAnswer(answer) {
  return [...answer].sort().join("");
}

function isCorrect(selected, answer) {
  return normalizeAnswer(selected) === normalizeAnswer(answer);
}

function percent(value, total) {
  if (!total) return "0%";
  return `${Math.round((value / total) * 100)}%`;
}

function getChapterForQuestion(question) {
  if (question.chapterId) return getChapter(question.chapterId);
  return questionBank.find((chapter) => chapter.questions.some((item) => item.id === question.id)) || getChapter(state.chapterId);
}

function getAnswerText(question) {
  return question.answer
    .map((key) => {
      const option = question.options.find((item) => item.key === key);
      return option ? `${key}. ${option.text}` : key;
    })
    .join("；");
}

function getExplanation(question) {
  if (question.explanation) return question.explanation;

  const chapter = getChapterForQuestion(question);
  const correctOptions = question.answer
    .map((key) => question.options.find((item) => item.key === key)?.text || key)
    .join("、");

  return `本题考查${chapter.shortTitle}的核心表述。题干关键词与“${correctOptions}”对应，因此应选${question.answer.join("、")}。复习时重点记住标准表述，避免被相近概念干扰。`;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderChapters() {
  els.chapterList.innerHTML = questionBank
    .map((chapter) => {
      const practiced = chapter.questions.filter((q) => progress.stats[q.id]).length;
      const className = chapter.id === state.chapterId ? "chapter-button active" : "chapter-button";
      return `
        <button class="${className}" data-chapter="${chapter.id}">
          <span class="chapter-title">
            <strong>${chapter.shortTitle}</strong>
            <span>${practiced}/${chapter.questions.length} 已练</span>
          </span>
          <span class="chapter-badge">${chapter.questions.length}题</span>
        </button>
      `;
    })
    .join("");

  document.querySelectorAll("[data-chapter]").forEach((button) => {
    button.addEventListener("click", () => {
      const chapterId = button.dataset.chapter;
      startChapter(chapterId);
    });
  });
}

function renderSummary() {
  if (!els.totalQuestions || !els.answeredQuestions || !els.accuracyRate || !els.mistakeCount) return;

  const total = allQuestions.length;
  const stats = Object.values(progress.stats);
  const answered = stats.length;
  const correct = stats.filter((item) => item.lastCorrect).length;
  const mistakes = Object.keys(progress.mistakes).length;

  els.totalQuestions.textContent = total;
  els.answeredQuestions.textContent = answered;
  els.accuracyRate.textContent = percent(correct, answered);
  els.mistakeCount.textContent = mistakes;
}

function setMode(mode) {
  state.mode = mode;
  els.navItems.forEach((item) => item.classList.toggle("active", item.dataset.mode === mode));

  if (mode === "chapter") {
    startChapter(state.chapterId);
    return;
  }

  if (mode === "random") {
    const shuffled = [...allQuestions].sort(() => Math.random() - 0.5).slice(0, Math.min(10, allQuestions.length));
    startQueue("random", shuffled, "随机练习", "全部题库随机抽题");
    return;
  }

  if (mode === "mistakes") {
    const mistakes = allQuestions.filter((question) => progress.mistakes[question.id]);
    startQueue("mistakes", mistakes, "错题本", "自动收录答错题目");
    return;
  }

  if (mode === "favorites") {
    const favorites = allQuestions.filter((question) => progress.favorites[question.id]);
    startQueue("favorites", favorites, "收藏题", "你标记的重点题");
    return;
  }

  if (mode === "exam") {
    const paper = [...allQuestions].sort(() => Math.random() - 0.5);
    startQueue("exam", paper, "模拟考试", "随机组卷，提交后即时判分");
  }
}

function startChapter(chapterId) {
  const chapter = getChapter(chapterId);
  state.chapterId = chapter.id;
  startQueue(
    "chapter",
    chapter.questions.map((question) => ({ ...question, chapterId: chapter.id })),
    chapter.title,
    chapter.meta
  );
}

function startQueue(mode, queue, title, meta) {
  state.mode = mode;
  state.queue = queue;
  state.index = 0;
  state.selected = [];
  state.sessionAnswers = {};
  state.sessionScore = 0;
  els.navItems.forEach((item) => item.classList.toggle("active", item.dataset.mode === mode));
  els.modeLabel.textContent = modeName(mode);
  els.setTitle.textContent = title;
  els.sheetTitle.textContent = title.replace("练习题", "");
  renderChapters();
  renderQuestion(meta);
  updatePracticeUrl(mode);
}

function modeName(mode) {
  const map = {
    chapter: "章节练习",
    random: "随机练习",
    mistakes: "错题本",
    favorites: "收藏题",
    exam: "模拟考试",
  };
  return map[mode] || "章节练习";
}

function updatePracticeUrl(mode) {
  if (!hasPracticeShell || !window.history?.replaceState) return;
  const params = new URLSearchParams();
  params.set("mode", mode);
  if (mode === "chapter") params.set("chapter", state.chapterId);
  window.history.replaceState(null, "", `${window.location.pathname}?${params.toString()}`);
}

function initPracticeFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const mode = params.get("mode") || "chapter";
  const chapterId = params.get("chapter") || state.chapterId;

  if (mode === "chapter") {
    startChapter(chapterId);
    return;
  }

  if (["random", "mistakes", "favorites", "exam"].includes(mode)) {
    setMode(mode);
    return;
  }

  startChapter(chapterId);
}

function renderQuestion(metaText = "") {
  renderSummary();

  if (!state.queue.length) {
    els.questionIndex.textContent = "0 / 0";
    els.sessionScore.textContent = "本轮 0 分";
    els.progressFill.style.width = "0%";
    els.questionType.textContent = "暂无题目";
    els.questionPoint.textContent = metaText;
    els.questionText.textContent = "";
    els.optionsList.innerHTML = `<div class="empty-state">当前模式下还没有题目。可以先完成章节练习，系统会自动生成错题本；也可以在答题时收藏重点题。</div>`;
    els.feedback.hidden = true;
    els.answerGrid.innerHTML = "";
    els.prevQuestion.disabled = true;
    els.nextQuestion.disabled = true;
    els.submitAnswer.disabled = true;
    els.favoriteQuestion.disabled = true;
    renderStatus();
    return;
  }

  const question = getCurrentQuestion();
  const questionId = getQuestionId(question);
  const sessionAnswer = state.sessionAnswers[questionId];
  const answered = Boolean(sessionAnswer);
  const selected = answered ? sessionAnswer.selected : state.selected;
  const chapter = getChapter(question.chapterId || state.chapterId);
  progress.lastIndex = state.index;
  saveProgress();

  els.questionIndex.textContent = `${state.index + 1} / ${state.queue.length}`;
  els.sessionScore.textContent = `本轮 ${state.sessionScore} 分`;
  els.progressFill.style.width = `${((state.index + 1) / state.queue.length) * 100}%`;
  els.questionType.textContent = question.type === "multiple" ? "多选题" : "单选题";
  els.questionPoint.textContent = `${question.points} 分`;
  els.questionText.textContent = question.text;
  els.sheetTitle.textContent = chapter.shortTitle;
  els.favoriteQuestion.textContent = progress.favorites[questionId] ? "★ 已收藏" : "☆ 收藏";
  els.favoriteQuestion.disabled = false;
  els.submitAnswer.disabled = answered;
  els.submitAnswer.textContent = answered ? "已提交" : "提交答案";
  els.prevQuestion.disabled = state.index === 0;
  els.nextQuestion.disabled = state.index === state.queue.length - 1;

  els.optionsList.innerHTML = question.options
    .map((option) => {
      const selectedClass = selected.includes(option.key) ? "selected" : "";
      const resultClass = answered
        ? question.answer.includes(option.key)
          ? "correct"
          : selected.includes(option.key)
            ? "wrong"
            : ""
        : "";
      return `
        <button class="option-button ${selectedClass} ${resultClass}" data-option="${option.key}" ${answered ? "disabled" : ""}>
          <span class="option-key">${option.key}</span>
          <span class="option-text">${option.text}</span>
        </button>
      `;
    })
    .join("");

  document.querySelectorAll("[data-option]").forEach((button) => {
    button.addEventListener("click", () => selectOption(button.dataset.option));
  });

  renderFeedback(question, sessionAnswer);
  renderAnswerSheet();
  renderStatus();
}

function selectOption(optionKey) {
  const question = getCurrentQuestion();
  if (!question) return;

  if (question.type === "multiple") {
    state.selected = state.selected.includes(optionKey)
      ? state.selected.filter((key) => key !== optionKey)
      : [...state.selected, optionKey];
  } else {
    state.selected = [optionKey];
  }

  renderQuestion();
}

function submitAnswer() {
  const question = getCurrentQuestion();
  if (!question || !state.selected.length) return;

  const questionId = getQuestionId(question);
  const correct = isCorrect(state.selected, question.answer);

  state.sessionAnswers[questionId] = {
    selected: [...state.selected],
    correct,
  };

  if (correct) {
    state.sessionScore += question.points;
    delete progress.mistakes[questionId];
  } else {
    progress.mistakes[questionId] = true;
  }

  progress.stats[questionId] = {
    attempts: (progress.stats[questionId]?.attempts || 0) + 1,
    correct: (progress.stats[questionId]?.correct || 0) + (correct ? 1 : 0),
    lastCorrect: correct,
    updatedAt: new Date().toISOString(),
  };

  saveProgress();
  renderQuestion();
}

function renderFeedback(question, sessionAnswer) {
  if (!sessionAnswer) {
    els.feedback.hidden = true;
    els.feedback.className = "feedback";
    els.feedback.textContent = "";
    return;
  }

  const correct = sessionAnswer.correct;
  const answerText = getAnswerText(question);
  const explanation = getExplanation(question);
  els.feedback.hidden = false;
  els.feedback.className = `feedback ${correct ? "correct" : "wrong"}`;
  els.feedback.innerHTML = `
    <strong>${correct ? "回答正确" : "回答错误"}</strong>
    <div>正确答案：${escapeHtml(answerText)}</div>
    <div>解析：${escapeHtml(explanation)}</div>
  `;
}

function renderAnswerSheet() {
  els.answerGrid.innerHTML = state.queue
    .map((question, index) => {
      const answer = state.sessionAnswers[question.id];
      const status = answer ? (answer.correct ? "done" : "missed") : "";
      const current = index === state.index ? "current" : "";
      return `<button class="answer-dot ${status} ${current}" data-jump="${index}">${index + 1}</button>`;
    })
    .join("");

  document.querySelectorAll("[data-jump]").forEach((button) => {
    button.addEventListener("click", () => {
      state.index = Number(button.dataset.jump);
      state.selected = [];
      renderQuestion();
    });
  });
}

function renderStatus() {
  const total = state.queue.length;
  const answered = Object.keys(state.sessionAnswers).length;
  const correct = Object.values(state.sessionAnswers).filter((answer) => answer.correct).length;
  const wrong = answered - correct;

  els.chapterStatus.innerHTML = `
    <div class="status-row"><span>本组题量</span><strong>${total}</strong></div>
    <div class="status-row"><span>已提交</span><strong>${answered}</strong></div>
    <div class="status-row"><span>本组正确率</span><strong>${percent(correct, answered)}</strong></div>
    <div class="status-row"><span>本组错题</span><strong>${wrong}</strong></div>
  `;
}

function go(delta) {
  const next = state.index + delta;
  if (next < 0 || next >= state.queue.length) return;
  state.index = next;
  state.selected = [];
  renderQuestion();
}

function toggleFavorite() {
  const question = getCurrentQuestion();
  if (!question) return;
  const questionId = getQuestionId(question);
  if (progress.favorites[questionId]) {
    delete progress.favorites[questionId];
  } else {
    progress.favorites[questionId] = true;
  }
  saveProgress();
  renderQuestion();
}

function restartSet() {
  state.index = 0;
  state.selected = [];
  state.sessionAnswers = {};
  state.sessionScore = 0;
  renderQuestion();
}

function bindEvents() {
  els.navItems.forEach((item) => {
    item.addEventListener("click", () => setMode(item.dataset.mode));
  });

  els.prevQuestion?.addEventListener("click", () => go(-1));
  els.nextQuestion?.addEventListener("click", () => go(1));
  els.submitAnswer?.addEventListener("click", submitAnswer);
  els.favoriteQuestion?.addEventListener("click", toggleFavorite);
  els.restartSet?.addEventListener("click", restartSet);
  els.quickStart?.addEventListener("click", () => startChapter(state.chapterId));
  els.resetProgress?.addEventListener("click", () => {
    const confirmed = window.confirm("确定要清空本机练习记录吗？题库不会被删除。");
    if (!confirmed) return;
    progress = { stats: {}, mistakes: {}, favorites: {}, lastIndex: 0 };
    saveProgress();
    restartSet();
  });

  window.addEventListener("keydown", (event) => {
    const key = event.key.toUpperCase();
    if (["A", "B", "C", "D"].includes(key)) selectOption(key);
    if (event.key === "Enter") submitAnswer();
    if (event.key === "ArrowLeft") go(-1);
    if (event.key === "ArrowRight") go(1);
  });
}

